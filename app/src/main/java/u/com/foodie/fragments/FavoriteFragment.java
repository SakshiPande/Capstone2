    private void getFavorites() {
   MainViewModel mainViewModel = ViewModelProviders.of(this).get(MainViewModel.class);
        mainViewModel.getFavoriteRes().observe(this, new Observer<List<RestaurantDB>>() {
            @Override
            public void onChanged(@Nullable List<RestaurantDB> restaurantDBList) {
                if(restaurantDBList!=null) {
                    if (restaurantDBList.size() != 0) {
                        hasFav=true;
                        if (rvFav != null) {
                            rvFav.setHasFixedSize(true);
                            LinearLayoutManager linearLayoutManager = new LinearLayoutManager(getActivity().getApplicationContext());
                            rvFav.setLayoutManager(linearLayoutManager);
                            rvFav.setAdapter(new FavoritesAdapter(restaurantDBList, getActivity().getApplicationContext()));
                        }
                    }
                }

            }
        });

    }

